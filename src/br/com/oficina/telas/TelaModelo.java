    private void txtnomemodeloKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnomemodeloKeyPressed
        String modelo = "%" + txtnomemodelo.getText() + "%";

        ModeloDAO dao = new ModeloDAO();
        List<Modelo> list = dao.consultaModelo(modelo);
        DefaultTableModel dados = (DefaultTableModel) tabelaModelo.getModel();
        dados.setNumRows(0);

        for (Modelo m : list) {
            dados.addRow(new Object[]{
                m.getId(),
                m.getDescricacao(),
                m.getPotencia(),
                m.getFabricante(),
                m.getAno()

            });

        }
    }//GEN-LAST:event_txtnomemodeloKeyPressed

