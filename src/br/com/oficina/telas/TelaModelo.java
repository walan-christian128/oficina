    public void listar() {
        ModeloDAO dao = new ModeloDAO();
        List<Modelo> list = dao.listaModelo();
        DefaultTableModel dados = (DefaultTableModel) tabelaModelo.getModel();
        dados.setNumRows(0);

        for (Modelo m : list) {
            dados.addRow(new Object[]{
                m.getId(),
                m.getDescricacao(),
                m.getPotencia(),
                m.getFabricante(),
                m.getAno()

            });

        }

    }

    public void setar() {

        int setar = tabelaModelo.getSelectedRow();
        txtcodigom.setText(tabelaModelo.getModel().getValueAt(setar, 0).toString());
        txtdescricacao.setText(tabelaModelo.getModel().getValueAt(setar, 1).toString());
        txtpotencia.setText(tabelaModelo.getModel().getValueAt(setar, 2).toString());
        cbFabrocante.setSelectedItem(tabelaModelo.getModel().getValueAt(setar, 3).toString());
        txtano.setText(tabelaModelo.getModel().getValueAt(setar, 4).toString());

    }

    public void limpar() {
        txtano.setText(null);
        txtdescricacao.setText(null);
        txtpotencia.setText(null);
        cbFabrocante.setSelectedItem(null);
        txtano.setText(null);
        txtcodigom.setText(null);
    }

