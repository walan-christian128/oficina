    private void btnattActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnattActionPerformed
        Modelo mod = new Modelo();
        mod.setId(Integer.parseInt(txtcodigom.getText()));
        mod.setDescricacao(txtdescricacao.getText());
        mod.setPotencia(txtpotencia.getText());
        mod.setFabricante(cbFabrocante.getSelectedItem().toString());
        mod.setAno(txtano.getText());

        ModeloDAO dao = new ModeloDAO();
        dao.alterarModelo(mod);
        limpar();

    }//GEN-LAST:event_btnattActionPerformed

    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisarActionPerformed
        String codigo = JOptionPane.showInputDialog("DIGITE O NUMERO DO MODELO");
        ModeloDAO dao = new ModeloDAO();
        Modelo mod = new Modelo();
        mod = dao.pesquisarModelo(codigo);

        if (mod != null) {
            txtcodigom.setText(String.valueOf(mod.getId()));
            txtdescricacao.setText(mod.getDescricacao());
            txtpotencia.setText(mod.getPotencia());
            cbFabrocante.setSelectedItem(mod.getFabricante());
            txtano.setText(mod.getAno());
         
        } else {
            JOptionPane.showMessageDialog(null, "MODELO NÃO ENCONTRADO");
        }

     


    }//GEN-LAST:event_btnpesquisarActionPerformed

